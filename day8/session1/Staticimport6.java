import static java.lang.Math.*;

public class Staticimport6 {
    
    public static void main(String[] args) {
        
        int a = 9;
        double sa = sqrt(a);
        double pa = pow(a,5);
        System.out.println("Square root of " +a +"is" +sa);
        System.out.println(a +" to the power 5 is "+pa);

    }

}
